#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t		i;

	i = 0;
	while (ft_strlen(s) >= i)
	{
		if ((char)s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
